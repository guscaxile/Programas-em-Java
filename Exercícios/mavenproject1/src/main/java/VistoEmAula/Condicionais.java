package VistoEmAula;


public class Condicionais {

    public static void main(String[] args) {

        Integer numero1 = 10;
        Integer numero2 = 10;

        if (numero1 == numero2) {

            System.out.println("São iguais mesmo!");
        } else {

            System.out.println("Não são iguais!");
        }
    }
}

